// JavaScript Math Rounding up
// rounding down
var a = Math.floor(3.2);
var b = Math.floor(3.7);
// rounding to the nearest number
var c = Math.round(3.5);
var d = Math.round(5.3);
// consultant to the top
var e = Math.ceil(2.7);
var f = Math.ceil(2.2);

// check the results in the console
console.log(a);
console.log(b);
console.log(c);
console.log(d);
console.log(e);
console.log(f);
// JavaScript Function with Parameter
// membuat fungsi dengan parameter
// a dan b ini adalah senbuah parameter
var count = (a, b) => {
    resultCount = a * b;
    alert("Hasil dari a * b = " + resultCount);
}

// kemudian memanggil parameter tersebut seperti ini
count(2, 3); //  2 adalah a, dan 3 adalah b
// JavaScript Array Method Sort
var alphabets = ['a', 'd', 'b', 'c', 'e', 'g', 'h', 'f'];
var numbers = [8, 7, 4, 3, 1, 2, 5, 9, 6];

document.write("Data before sorting<br>");
document.write(alphabets + "<br>");
document.write(numbers + "<br>");

document.write("Data after sorting using the sorting method<br>");
document.write(alphabets.sort() + "<br>");
document.write(numbers.sort());